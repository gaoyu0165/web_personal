      <p>我一直认为，今后金属架里面，钛架会成为主流，一是生物相容，二是轻巧，三是档次。店里开价800或1000以上的，也多是钛架但是，钛架和钛架的差别，可是大的去了</p>
      <p>1 目前最贵最好的钛架：B钛，超薄、超轻，但是加工难度大、成型难度大，需要更好的设备和更高的坏品率，所以成本也相对是最高的，一个超薄超轻的钛片架子，大点的店面，都1500以上了，如果有点品牌，更高</p>
      <p>2 纯钛：一般以钛柱子的形式出现，比其他金属轻很多，但比钛片重，形状也没有片状的好看，这是很多店里的主流的钛产品</p>
      <p>3 合金钛：就是钛和其他金属的合金，很多是比较低端的钛产品，但也有做的好的，我是说大致的情况</p>
