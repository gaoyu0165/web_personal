      <p>眼镜框材质总的来说分金属类和塑料两大类</p>
      <p>金属类分为：钛材质和合金材料，不锈钢材质</p>
      <p>塑胶类分为：厚重的板材、轻薄的TR材质、最近才开始的ULTEAM材料，以及其他塑胶制品类眼镜</p>
      <p>钛材质分为：合金钛、纯钛、B钛（超薄超轻）</p>
      <p>总之，目前就材料档次而言，超薄B钛材的档次最高、纯钛其次、板材类为：超轻</p>
      <p>薄的ULTEAM材料（美国通用的材料，韩国生产，国内还生产不好)，板材次之</p>
      <p>但从工艺和做工方面，又不一定最好的材质，一定是最贵的眼镜，有的名牌眼镜，就是塑胶板材，但每个细节很完美，也很昂贵</p>
      <p>市面上流行过一种说法，说中国丹阳的眼镜按斤卖，下面是我整理的一点信息，希望看完后，让大家知道：</p>
      <p>镜架是贴脸的，涉及美观、气质和健康</p>
      <p>也让大家知道，垃圾的东西，不是最好的选择</p>
      <p>还有，镜架，特别是好的镜架，不是传说中的那么的廉价：</p>
      <p>眼镜框材质总的来说分金属和塑料两大类，早期使用的眼镜框基本都是以含铅含锡量较高的铜合金制成，当时的造价非常低廉，2000年以前在丹阳8毛钱可买到镜框，当时零售价在45元左右。由于铅对人伤害很大，铜也太贵了，现在都使用蒙纳尔合金做，这种合金是国外研究出来的，其实也就是铜镍合金，不易上锈而已。很多人进口这种合金丝，后来就仿制，不过中国人的老毛病就是偷工减料，铅含量仍然超标，带金属边眼镜的人，如果不常注意清洁，镜架上的重金属会对面部皮肤以至全身血液构成严重危胁。我敢打赌，没有一个长期戴眼镜的中国男人脸部是光滑的。</p>
