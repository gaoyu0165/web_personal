      <h5>第六章 镜架之三：金属类镜架</h5>
      <p>你还记得戴过铁眼镜吗？戴的锈迹斑斑的，相信很多人老眼镜人都戴过</p>
      <p>目前的金属类，其实都很不错，大部分是不锈钢为主，或者是金属合金的，经过电镀，再加工等</p>
      <p>金属材料和板材材料，是目前普通镜架的主流，大部分人戴的是这两类金属材料，是店面镜架的主流，它加工容易、材料成本低、电镀喷涂的工艺都很成熟，我在公交车里看到的，大部分戴的都是普通金属镜架</p>
      <p>金属镜架也可以做的很漂亮，也可以做的很高级，如很好的设计、喷涂、丝印等工艺，让金属镜架显得很高级金属镜架我就不多说了，是比较普通的一种，也是大众所选择的一种</p>
      <p>4 其他材料：</p>
      <p>现在看来，用竹子或檀木做的可能要环保和健康一些，可是戴不多长时间</p>
      <h5>第七章 究竟怎样识别一副好镜架与差镜架</h5>
      <img src="http://himg2.huanqiu.com/attachment2010/2019/0128/16/01/20190128040148218.jpeg" alt>
      <p>这个是网友们最关心的，也许你会说：你别啰嗦了，直接告诉我怎样识别好坏，</p>
      <p>别急，你不懂一些基本的发动机原理和诸如速度与撞击等基本的知识，你怎么识别一个好车，它为什么会好</p>
      <p>啰嗦几句，接下来，我告诉你怎样识别镜架的好坏，从整体识别：</p>
      <p>决定镜架的质量主要有三个因素：材质、工艺细节、造型</p>
      <p>材质刚才我们说过了，现在就给你说说其他方面</p>
      <p>铰链：眼镜虽然不是综合的高科技，但是细节还是要重视，比如铰链--就是那个打开和合拢的关节，这些好的和坏的，都有很大的区别，进口的如能有100万次开合，国产可能就是50万次（我不记得具体数据）</p>
      <p>鼻托：就是那几一对透明的东西，店里都免费配，但是，这个东西和批发天天接触，像日本韩国都讲究无毒的材料，也是由分别的。</p>
      <p>电镀喷涂：就是指金属镜架，都需要电镀上颜色，喷涂，这个工艺的好坏，也决定着镜架的好坏。</p>
      <p>螺丝：里面的小螺丝，大概有4个把（铰链和鼻托），其实也很重要，当然一般都没有问题，就是有的不好的，会生锈，影响美观和健康。</p>
      <p>丝印或激光雕刻：指上面的标志或数据，有的是丝印的，容易汗水而掉色，不影响使用，但感觉不好，有的是镭射雕刻，基本不会褪色，所以，看看这些细节，都能大概判断镜架的好坏。</p>
      <p>看到了吗？？ 镜架主要是材料，但工艺和细节很重要，</p>
      <p>有的品牌的眼镜，其实就是普通的金属，但是细节他做的很完美，整个眼镜业非常不错，当然，价格也很高。</p>
      <p>--追求材质，不是唯一的，眼镜是综合的</p>
      <h5>第八章 究竟如何识别眼镜好坏的总结</h5>
      <img
        src="https://simg314.magcasa.com/content_images/2018/01/28/535826/1517121543_2587.jpg"
        alt
      >
      <p>到底如何去辨别一副眼镜的好坏呢，我总结了一下：</p>
      <p>1 问问材质，轻薄、生物相容（如钛，TR、ULTEAM），是主流方向。</p>
      <p>2 多看细节：铰链声音、光洁度、电镀外表感觉、LOGO是雕刻还是丝印，这些你都能感觉到这个眼镜品质。</p>
      <p>3 不要一听钛就掏口袋，要看什么钛、重量如何、做工如何，也不要一定金属，就觉得廉价，同样，要看全面。</p>
      <p>眼镜除了质量之外，还有就是款型，这个就是人见人爱了</p>
      <p>现在款型的趋势大致是：</p>
      <p>轻--黑---雅。</p>
      <p>基本都是选择轻巧的。</p>
      <p>黑色或素色，是大家多数的选择。</p>
      <p>雅致的，不那么夸张的，也是基本的选择。</p>
      <h5>第九章 镜片--镜片的分类</h5>
      <p>镜片---眼镜的灵魂</p>
      <p>1 为啥说灵魂，因为镜片才是功能型的，试想如果没有发明镜架，你用一个跟橡皮筋，棒这两个镜片，是否也是一副眼镜呢？？---镜片重要，我不多说，连镜片的重要也不明白的，多说也无用</p>
      <p>2 镜片的进口和国产：</p>
      <p>90%的人都戴的国产镜片，为啥，进口太贵了，但是你大致放心，正规的工厂的国产镜片，其实这么多年下来，质量的基本面是过关的，不要过分贬低国产，还有，你也只花那个钱啊</p>
      <p>进口镜片：进口的，知名品牌大家也都听说一些里，帖子里我不合适提，其中，日本和德国的字好----从化妆品到汽车，那个不是日本和德国好呢</p>
      <p>这么说吧，进口镜片，是要好一些，但是否好到要1-2万一副，那就看你的口袋了，实话说，值不值，我不明说，你得自己掂量</p>
      <p>折射率：我们听过1.51、1.61、1.74等术语，其实是指折射率，就是同样500度，高的更薄</p>
      <p>--但对于200度的，你需要1.74的吗？动辄2000以上，所以，多了解，就不会被忽悠</p>
      <p>明白我的意思吗？？</p>
      <p>折射率就是影响镜片厚度和重量，不影响功能！！</p>
      <h5>第十章 怎样辨别镜片的好坏</h5>
      <img
        src="https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcSx_aZ3Hwd46AxqTpjk3YvudDmg-FZx6G70U9DE9gNh5Yl9avDwqA"
        alt
      >
      <p>怎么买镜片以及怎么判断镜片的质量</p>
      <p>我们配镜的时候，很多人根本都没看镜片的包装、品名、日期、等，因为都是加工好的</p>
      <p>---建议下次配镜，一定好好看看，最好网络上查查，这个牌子的，哪儿产的，是否有防伪等，虽然你说没用，但是，多了解一下，特别是让店里把包装和查询防伪等给你留着，也是一点监督吧</p>
      <p>戴2年的眼镜，自己的镜片是哪儿产的、什么折射率、啥都不知道，光知道花了400还是800</p>
      <p>这可是有点荒唐！！</p>
      <p>当然，肉眼是很难判断一个镜片的好坏的，只能从以下几个方面来看：</p>
      <p>1 看看整体包装：让他给你看看镜片的整体包装，感受一下</p>
      <p>2 看看出厂地点：出厂在中国、还是进口、是中国上海，还是中国浙江等，</p>
      <p>3 看看防伪标志：防伪标志，不一定意味着就是好东西，但看看，把握一下</p>
      <p>4 看看生产工厂：查询下生产工厂，地址、新闻等等</p>
      <p>总之，镜片的判断，普通消费者很难，但你提出这些要求，店家至少会认为你比较专业，因此，不太敢给你弄低质的冒充高质的</p>
      <p>眼镜行业是有些奇怪。顾客和商家，互相不信任，互相埋怨，甚至，顾客仇视商家</p>
      <p>这一切，都应该从普及一些知识来开始</p>
      <p>知己知彼，百战不殆</p>
      <h5>第十一章 镜片的加工</h5>
      <p>镜片的加工：也就是打磨和安装，这点大家不要担心，现在都是比较自动化的设备，并且，深圳大部分眼镜店，自己都不可能设独立的加工，都是第三方加工的，所以品质都不会有什么大问题。眼镜片是否舒服其实取决于制镜师的本事。看现在很多学生从一二百度一步步涨到六七百度，除与现在学业重有关外，与验光结果不准，制镜不合理有绝对大的关系。</p>
      <p>同样一副镜片，同样一个验光结果（后面我会专门讲讲验光的知识），不同的制镜师用不同的工具做出来，效果是完全不一样的。</p>
