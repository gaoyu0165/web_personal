      <p>店面贵，是因为成本太高，其实合理的价格，也不用那么贵。</p>
      <h5>第五章 镜架之塑胶架</h5>
      <p>1 塑胶大类：塑胶是个很广泛的含义，非金属的镜架，基本就是塑胶大类的</p>
      <p>A 传统的塑胶，就是板材，很厚的那种，当然做的好的，也很有气质，但是还是很重，塑胶和金属铰链，有时候容易坏---但板材，还是目前普通眼镜的主流之一</p>
      <p>B TR90：是今年韩国兴起的新材料，很轻、很软、不伤人，可以是黑色的、可以是透明的，TR90为韩国做的最好，很多韩剧里面，那些看起来很轻的眼镜其实就是TR90的材料</p>
      <p>---在十年面上，现在大部分是中国生产的TR90，确实工艺方面和材质方面，比不过韩国，但是成本低，大部分店里，都卖国产的，同样配一副，利润高很多，而顾客，根本不明白韩国产和中国产。</p>
      <p>C ULTEM材料：是一种美国通用的航空材料，比TR更轻更柔然，很适合接触批发，遗憾，又是韩国和日本最早生产，当然成本也比较高，大约2011年底才开始来到中国，目前可以说很多内地的店面，根本店主都没听说过ULTEM材料，UTLEA对人体伤害最小，当然，和皮肤接触，一般的健康材料，都问题不大。</p>
