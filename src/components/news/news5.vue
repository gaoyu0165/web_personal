      <p>中国是全世界最大的眼镜生产国和出口国，全球80%的眼镜，都是中国生产</p>
      <p>但高端的眼镜镜架，设计和品牌在境外，国内的工作来加工</p>
      <p>中国的眼镜分布在：</p>
